const cropLabels: Record<string, string> = {
  Wheat: 'crop.wheat',
  Rice: 'crop.rice',
  Cotton: 'crop.cotton',
  Sugarcane: 'crop.sugarcane',
  Soybean: 'crop.soybean'
};

export const translate = (language: LanguageCode, key: string, params?: Record<string, string | number>) => {
  const entry = translations[key];
  const template = entry ? entry[language] ?? entry.en : key;

  if (!params) return template;

  return Object.entries(params).reduce(
    (text, [paramKey, paramValue]) => text.replaceAll(`{${paramKey}}`, String(paramValue)),
    template,
  );
};

export const useTranslations = () => {
  const { language } = useAppContext();

  return {
    language,
    t: (key: string, params?: Record<string, string | number>) => translate(language, key, params),
    translateCrop: (crop: string) => {
      const key = cropLabels[crop];
      return key ? translate(language, key) : crop;
    },
  };
};






